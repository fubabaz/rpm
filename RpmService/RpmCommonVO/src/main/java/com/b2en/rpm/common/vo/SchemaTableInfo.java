package com.b2en.rpm.common.vo;

import java.io.Serializable;

import com.b2en.common.vo.AbstractCommonVO;

public class SchemaTableInfo extends AbstractCommonVO implements Serializable {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String tblNm;
	private String tblComnt;
	private String colNm;
	private String colComnt;
	private String owner;
	private String tblType;
	
	public String getTblNm() {
		return tblNm;
	}
	public void setTblNm(String tblNm) {
		this.tblNm = tblNm;
	}
	public String getTblComnt() {
		return tblComnt;
	}
	public void setTblComnt(String tblComnt) {
		this.tblComnt = tblComnt;
	}
	public String getColNm() {
		return colNm;
	}
	public void setColNm(String colNm) {
		this.colNm = colNm;
	}
	public String getColComnt() {
		return colComnt;
	}
	public void setColComnt(String colComnt) {
		this.colComnt = colComnt;
	}
	public String getOwner() {
		return owner;
	}
	public void setOwner(String owner) {
		this.owner = owner;
	}
	public String getTblType() {
		return tblType;
	}
	public void setTblType(String tblType) {
		this.tblType = tblType;
	}
	
}
